#[derive(Debug, Deserialize)]
struct CreateItemRequest {
    title: String,
}

#[derive(Debug, Serialize)]
struct Item {
    id: Uuid,
    owner_id: Uuid,
    title: String,
}

#[derive(Debug, Error)]
enum ItemsError {
    #[error("invalid request: {0}")]
    Invalid(String),
    #[error("items service unavailable: {0}")]
    Unavailable(String),
    #[error("missing required scope `{0}`")]
    Forbidden(&'static str),
}

impl IntoResponse for ItemsError {
    fn into_response(self) -> Response {
        let (status, code) = match &self {
            Self::Invalid(_) => (StatusCode::BAD_REQUEST, "items.invalid"),
            Self::Unavailable(_) => (StatusCode::SERVICE_UNAVAILABLE, "items.unavailable"),
            Self::Forbidden(_) => (StatusCode::FORBIDDEN, "items.forbidden"),
        };
        api_error(status, code, self.to_string())
    }
}

async fn create_item(
    State(state): State<ItemsState>,
    Extension(user): Extension<AuthenticatedUser>,
    Extension(context): Extension<RequestContext>,
    Json(input): Json<CreateItemRequest>,
) -> Result<(StatusCode, Json<Item>), ItemsError> {
    let title = input.title.trim();
    if title.is_empty() {
        return Err(ItemsError::Invalid("title is required".into()));
    }
    let database = state
        .database
        .as_ref()
        .ok_or_else(|| ItemsError::Unavailable("database is not configured".into()))?;
    let count = sqlx::query_scalar::<_, i64>(
        "SELECT COUNT(*) FROM items.items WHERE owner_id = $1 AND deleted_at IS NULL",
    )
    .bind(user.user_id)
    .fetch_one(database.pool())
    .await
    .map_err(|error| ItemsError::Unavailable(error.to_string()))?;
    if count >= i64::from(state.max_items) {
        return Err(ItemsError::Invalid(format!(
            "at most {} items are allowed",
            state.max_items
        )));
    }

    let id = Uuid::now_v7();
    let mut tx = database
        .pool()
        .begin()
        .await
        .map_err(|error| ItemsError::Unavailable(error.to_string()))?;
    sqlx::query("INSERT INTO items.items (id, owner_id, title) VALUES ($1, $2, $3)")
        .bind(id)
        .bind(user.user_id)
        .bind(title)
        .execute(&mut *tx)
        .await
        .map_err(|error| ItemsError::Unavailable(error.to_string()))?;
    publish_in_tx(
        &mut tx,
        &EventEnvelope::new(
            "items.item_created.v1",
            json!({ "id": id, "owner_id": user.user_id, "request_id": context.request_id }),
        ),
    )
    .await
    .map_err(|error| ItemsError::Unavailable(error.to_string()))?;
    tx.commit()
        .await
        .map_err(|error| ItemsError::Unavailable(error.to_string()))?;

    Ok((
        StatusCode::CREATED,
        Json(Item {
            id,
            owner_id: user.user_id,
            title: title.to_owned(),
        }),
    ))
}

async fn list_items(
    State(state): State<ItemsState>,
    Extension(user): Extension<AuthenticatedUser>,
) -> Result<Json<Value>, ItemsError> {
    let database = state
        .database
        .as_ref()
        .ok_or_else(|| ItemsError::Unavailable("database is not configured".into()))?;
    let rows = sqlx::query(
        "SELECT id, owner_id, title FROM items.items
         WHERE owner_id = $1 AND deleted_at IS NULL
         ORDER BY created_at DESC",
    )
    .bind(user.user_id)
    .fetch_all(database.pool())
    .await
    .map_err(|error| ItemsError::Unavailable(error.to_string()))?;
    let items = rows
        .into_iter()
        .map(|row| {
            Ok(Item {
                id: row
                    .try_get("id")
                    .map_err(|e| ItemsError::Unavailable(e.to_string()))?,
                owner_id: row
                    .try_get("owner_id")
                    .map_err(|e| ItemsError::Unavailable(e.to_string()))?,
                title: row
                    .try_get("title")
                    .map_err(|e| ItemsError::Unavailable(e.to_string()))?,
            })
        })
        .collect::<Result<Vec<_>, ItemsError>>()?;
    Ok(Json(json!({ "data": items })))
}

async fn admin_list_items(
    State(state): State<ItemsState>,
    Extension(principal): Extension<AdminPrincipal>,
) -> Result<Json<Value>, ItemsError> {
    require_scope(&principal, "items:read", ItemsError::Forbidden)?;
    let database = state
        .database
        .as_ref()
        .ok_or_else(|| ItemsError::Unavailable("database is not configured".into()))?;
    let rows = sqlx::query(
        "SELECT id, owner_id, title FROM items.items
         WHERE deleted_at IS NULL
         ORDER BY created_at DESC
         LIMIT 100",
    )
    .fetch_all(database.pool())
    .await
    .map_err(|error| ItemsError::Unavailable(error.to_string()))?;
    let items = rows
        .into_iter()
        .map(|row| {
            Ok(json!({
                "id": row.try_get::<Uuid, _>("id").map_err(|e| ItemsError::Unavailable(e.to_string()))?,
                "owner_id": row.try_get::<Uuid, _>("owner_id").map_err(|e| ItemsError::Unavailable(e.to_string()))?,
                "title": row.try_get::<String, _>("title").map_err(|e| ItemsError::Unavailable(e.to_string()))?,
            }))
        })
        .collect::<Result<Vec<_>, ItemsError>>()?;
    Ok(Json(json!({ "data": items })))
}

