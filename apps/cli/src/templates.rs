#[derive(Debug, Clone)]
pub struct TemplateContext {
    pub name: String,
    pub snake: String,
    pub pascal: String,
    pub runtime_dep: String,
    pub sdk_dep: String,
    pub manifest: ProjectManifest,
}

#[derive(Debug, Clone)]
pub enum BosonSource {
    Path(PathBuf),
    Git { url: String, rev: String },
}

impl TemplateContext {
    pub fn new(name: &str, source: BosonSource) -> Self {
        let (runtime_dep, sdk_dep) = match source {
            BosonSource::Path(root) => (
                format!(
                    "version = \"0.1.0\", path = \"{}/crates/runtime\"",
                    root.display()
                ),
                format!(
                    "version = \"0.1.0\", path = \"{}/crates/sdk\"",
                    root.display()
                ),
            ),
            BosonSource::Git { url, rev } => (
                format!(
                    "version = \"0.1.0\", git = \"{url}\", rev = \"{rev}\", package = \"boson-runtime\""
                ),
                format!(
                    "version = \"0.1.0\", git = \"{url}\", rev = \"{rev}\", package = \"boson-sdk\""
                ),
            ),
        };
        Self {
            name: name.to_owned(),
            snake: to_snake_case(name),
            pascal: to_pascal_case(name),
            runtime_dep,
            sdk_dep,
            manifest: ProjectManifest::for_name(name),
        }
    }

    fn render(&self, template: &str) -> String {
        template
            .replace("{{NAME}}", &self.name)
            .replace("{{SNAKE}}", &self.snake)
            .replace("{{PASCAL}}", &self.pascal)
            .replace("{{RUNTIME_DEP}}", &self.runtime_dep)
            .replace("{{SDK_DEP}}", &self.sdk_dep)
            .replace("{{SERVER_PKG}}", &self.manifest.server_package)
            .replace("{{WORKER_PKG}}", &self.manifest.worker_package)
            .replace("{{MIGRATE_PKG}}", &self.manifest.migrate_package)
            .replace("{{CAPABILITY_PKG}}", &format!("{}_items", self.snake))
    }
}

pub struct TemplateFile {
    pub relative_path: &'static str,
    pub contents: String,
}

pub fn render_project(ctx: &TemplateContext) -> Vec<TemplateFile> {
    let files = [
        (".boson/project.toml", PROJECT_TOML),
        ("Cargo.toml", WORKSPACE_TOML),
        ("README.md", README),
        ("rust-toolchain.toml", RUST_TOOLCHAIN),
        (".boson/config.yaml", CONFIG_LOCAL),
        (".gitignore", GITIGNORE),
        ("apps/server/Cargo.toml", SERVER_CARGO),
        ("apps/server/src/main.rs", SERVER_MAIN),
        ("apps/worker/Cargo.toml", WORKER_CARGO),
        ("apps/worker/src/main.rs", WORKER_MAIN),
        ("apps/migrate/Cargo.toml", MIGRATE_CARGO),
        ("apps/migrate/src/main.rs", MIGRATE_MAIN),
        ("capabilities/items/Cargo.toml", ITEMS_CARGO),
        ("capabilities/items/src/lib.rs", ITEMS_LIB),
        (
            "capabilities/items/migrations/20260718210000_items.sql",
            ITEMS_MIGRATION,
        ),
    ];
    files
        .into_iter()
        .map(|(relative_path, template)| TemplateFile {
            relative_path,
            contents: ctx.render(template),
        })
        .collect()
}

